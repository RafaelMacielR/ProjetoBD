    private void consultarBttActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultarBttActionPerformed
        // TODO add your handling code here:
        
        try {
            con = DriverManager.getConnection(url, user, password);
            String sql = "SELECT * FROM carona WHERE nome = ?";
        
            PreparedStatement cmd;
            
            cmd  = con.prepareStatement(sql);
            
            cmd.setString(1, caronaText.getText());
            
            ResultSet rs;
            
            rs = cmd.executeQuery();
            
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            
            model.setNumRows(0);
            
            while(rs.next()){
                
                model.addRow(
                        new Object[]{
                            rs.getString("codigo"),
                            rs.getString("origem"),
                            rs.getString("nome"), 
                            rs.getString("destino"), 
                            rs.getString("quantidade"), 
                            rs.getString("valor"),
                            rs.getString("datahora")
                        }
                );     
                        
                System.out.println("Nome:" + rs.getString("nome"));
                
                
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(MinhasCaronasMFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_consultarBttActionPerformed

