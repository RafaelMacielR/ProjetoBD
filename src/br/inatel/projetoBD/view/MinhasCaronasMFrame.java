    private void editarBttActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarBttActionPerformed
        
        try {
            con = DriverManager.getConnection(url, user, password);
            
            String sql = "UPDATE carona SET codigo = ?, origem = ?, nome = ?, destino = ?, quantidade = ?, valor = ?, datahora = ? WHERE nome = ?";
            
            PreparedStatement ps = con.prepareStatement(sql);
            
            String codigo, info, nome, marca, quantidade, valor, datahora;
            
            
            codigo = (String) jTable1.getValueAt(jTable1.getSelectedRow(), 0);
            info = (String) jTable1.getValueAt(jTable1.getSelectedRow(), 1);
            nome = (String) jTable1.getValueAt(jTable1.getSelectedRow(), 2);
            marca = (String) jTable1.getValueAt(jTable1.getSelectedRow(), 3);
            quantidade = (String) jTable1.getValueAt(jTable1.getSelectedRow(), 4);
            valor = (String) jTable1.getValueAt(jTable1.getSelectedRow(), 5);
            datahora = (String) jTable1.getValueAt(jTable1.getSelectedRow(), 6);
            
            ps.setString(8, caronaText.getText());
            
            ps.setInt(1, Integer.parseInt(codigo));
            ps.setString(2, info);
            ps.setString(3, nome);
            ps.setString(4, marca);
            ps.setInt(5, Integer.parseInt(quantidade));
            ps.setFloat(6, Float.valueOf(valor));
            ps.setString(7, datahora);
            
            ps.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Editado com sucesso!");
            
        } catch (SQLException ex) {
            System.out.println("Erro: "+ex.getMessage());
        }
        
    }//GEN-LAST:event_editarBttActionPerformed

